void StuffingFilter(List_int* scan)
{
	List_int *cur = scan, *cur1, *prev = scan;
	while(cur)
	{
		if(prev->content != 0xFF || cur->content != 0)
		{
            prev = cur;
		    cur  = cur->next;
		}
		else
		{
			cur1 = cur;
		    prev->next = cur->next;
		    prev = cur->next;
		    cur  = cur->next;
		    free(cur1);
		}
	}
}

uint8_t getByte()
{
	if(!TMP)
	{
		is_empty = 1;
		return 0;
	}
	else
	{
		uint8_t val = TMP->content;
		List_int *cur = TMP;
		TMP = TMP->next;
		free(cur);
		return val;
	}
}

uint8_t getBit()
{
	if(pos == 0)
	{
		val = getByte();
		if(is_empty == 1)
			return 0;
		else
			pos = 8;
	}
	uint8_t inter = val << (8 - pos);
		    inter = inter >> 7;
		    pos--;
	return inter;
}

void getLumDCValue(SYMBOL *input)
{
	uint8_t ptr_nxt, not_found, ad_dc, bit, cnt;
	//uint8_t numb;
	uint16_t vli;

	ptr_nxt = 0;
	not_found = 1;
	do
	{
		bit = getBit();
		if(is_empty)
			return;
		if(bit)
			ad_dc = LUM_DC1[ptr_nxt];
		else
			ad_dc = LUM_DC0[ptr_nxt];
		ptr_nxt = ad_dc >> 4;
		if(ptr_nxt == 0)
		{
			not_found = 0;
			cnt = ad_dc;
			(*input).VLC.NOZ = 0;
			(*input).VLC.amplitude = cnt;
		}
	}
	while(not_found);
	vli = 0;
	while(cnt)
	{
		bit = getBit();
		if(is_empty)
			return;
		vli = (vli << 1) | bit;
		cnt--;
	}
	(*input).VLI = vli;
}

void getLumACValue(List *input)
{
	uint8_t ptr_nxt,not_found,bit,cnt = 63,cnt1;
	//uint8_t numb;
	uint16_t vli,ad_ac;
	SYMBOL cur;
	List *cur_ac = input;
	do
	{
		ptr_nxt = 0;
		not_found = 1;
		do
		{
			bit = getBit();
			if(is_empty)
				return;
			if(bit)
				ad_ac = LUM_AC1[ptr_nxt];
			else
				ad_ac = LUM_AC0[ptr_nxt];
			ptr_nxt = ad_ac >> 8;
			if(ptr_nxt == 0)
			{
				not_found = 0;
				cnt1 = ad_ac & 0xF;
				cur.VLC.NOZ = ad_ac >> 4;
				cur.VLC.amplitude = cnt1;
				cnt -= (cur.VLC.NOZ + 1);
			}
		}
		while(not_found);
		vli = 0;
		while(cnt1)
		{
			bit = getBit();
			if(is_empty)
				return;
			vli = (vli << 1) | bit;
			cnt1--;
		}
		cur.VLI = vli;
		cur_ac = push(cur_ac, cur);
	}
	while(ad_ac && cnt);
}

