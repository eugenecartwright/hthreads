void getMCU(scancodeDC *DCDiff, scancodeAC *ACCoef, uint32_t cord)
{
	uint8_t i;
	for( i = 0; i < 4; i++)
	{
		getLumDCValue(&(*DCDiff).Y[cord]);
		if(is_empty)
		   return;
		(*ACCoef).Y[cord] = (List *)malloc(sizeof(List));
		(*ACCoef).Y[cord]->content.VLC.NOZ = 0;
		(*ACCoef).Y[cord]->content.VLC.amplitude = 0;
		(*ACCoef).Y[cord]->content.VLI= 0;
		(*ACCoef).Y[cord]->next = NULL;
		(*ACCoef).Y[cord]->previous = NULL;
		getLumACValue((*ACCoef).Y[cord]);
		cord++;
		if(is_empty)
		   return;
	}
	uint32_t pos = ((cord - 1) >> 2);
	getChrDCValue(&(*DCDiff).Cb[pos]);
	if(is_empty)
	   return;
	(*ACCoef).Cb[pos] = (List *)malloc(sizeof(List));
	(*ACCoef).Cb[pos]->content.VLC.NOZ = 0;
	(*ACCoef).Cb[pos]->content.VLC.amplitude = 0;
	(*ACCoef).Cb[pos]->content.VLI= 0;
	(*ACCoef).Cb[pos]->next = NULL;
	(*ACCoef).Cb[pos]->previous = NULL;
	getChrACValue((*ACCoef).Cb[pos]);
	if(is_empty)
	   return;
	getChrDCValue(&(*DCDiff).Cr[pos]);
	if(is_empty)
	   return;
	(*ACCoef).Cr[pos] = (List *)malloc(sizeof(List));
	(*ACCoef).Cr[pos]->content.VLC.NOZ = 0;
	(*ACCoef).Cr[pos]->content.VLC.amplitude = 0;
	(*ACCoef).Cr[pos]->content.VLI= 0;
	(*ACCoef).Cr[pos]->next = NULL;
	(*ACCoef).Cr[pos]->previous = NULL;
	getChrACValue((*ACCoef).Cr[pos]);
	if(is_empty)
	   return;
}

void Huffmann_decoder(List_int* SCAN_CODE, scancodeDC *DCDiff, scancodeAC *ACCoef)
{
	uint32_t i = 0;
	StuffingFilter(SCAN_CODE);
	TMP = SCAN_CODE;
	do
	{
		getMCU(DCDiff, ACCoef, i);
		i += 4;
	}
	while(is_empty == 0);
	i = 0;
}
